# 从键盘输入一个整数n(1≤n≤9)，打印出指定的菱形。
#
# Input
#
# 正整数n（1≤n≤9）。
#
# Output
#
# 指定的菱形。
#  第一行前面有n-1个空格，第二行有n-2个空格，依此类推。
#
# Sample Input
#
# 5
#
# Sample Output
#
#     *
#    ***
#   *****
#  *******
# *********
#  *******
#   *****
#    ***
#     *


n=int(input())
s=''
for i in range(1,2*n):
    if i<=n:
        s+=" "*(n-i)+"*"*(2*i-1)+'\n'
    else:
        s+=" "*(i-n)+"*"*((2*i-1)-4*(i-n))+'\n'
print(s)
